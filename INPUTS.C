#include<stdio.h>
int main()
{   

    // for integers
    int age;
    printf("enter age:");
    scanf("%d",&age);
    printf("age is %d \n",age);


    // for decimals
    float pi;
    printf("\nenter value of pi: ");
    scanf("%f",&pi);
    printf("value of pi is %f",pi);

    
    //for characters
    char star;
    printf("\n how star look like:");
    scanf("%c",&star);
    printf("symbol of star is %c",star);


return 0;
}