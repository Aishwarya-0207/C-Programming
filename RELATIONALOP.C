#include<stdio.h>
int main()
{
    //relational operators.

    //equal to.
    printf("%d \n",4==4);

    //greater than or equal to.
    printf("%d \n",4>=3);

    //less than or equal to.
    printf("%d \n",5<=4);

    //not equal to.
    printf("%d \n",5!=5);


    return 0;

}